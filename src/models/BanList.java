package models;

/**
 *
 * @author danie
 */
public class BanList 
{
    private String nickName;
    private String id;
    
    public BanList(String n, String id)
    {
        this.nickName = n;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }
}
